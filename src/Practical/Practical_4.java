package Practical;

public class Practical_4 {
	
	 public static void main(String[] args) {
	        
	        // 1. Using println (each line printed separately in a new line)
	        System.out.println("This is the first line of the paragraph.");
	        System.out.println("This is the second line of the paragraph.");

	        System.out.println(); // blank line for separation

	        // 2. Using print (without \n, so both stay on same line)
	        System.out.print("This is the first line of the paragraph.");
	        System.out.print("This is the second line of the paragraph.");

    }
}


